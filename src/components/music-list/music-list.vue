<template>
  <div class="music-list">
    <div class="back" @click="back">
      <i class="icon-back"></i>
    </div>
    <h1 class="title" v-html="title"></h1>
    <div class="bg-image" :style="bgStyle" ref="bgImage">
      <div class="play-wrapper">
        <div ref="playBtn" v-show="songs.length>0" class="play" @click="random">
          <i class="icon-play"></i>
          <span class="text">随机播放全部</span>
        </div>
      </div>
      <div class="filter" ref="filter"></div>
    </div>
    <div class="bg-layer" ref="layer"></div>
    <scroll class="list" ref="list" :data="songs" :probe-type="probeType" :listen-scroll="listenScroll" @scroll="scroll">
      <div class="song-list-wrapper">
        <song-list :songs="songs" @select="selectItem"></song-list>
      </div>
    </scroll>
  </div>
</template>

<script>
import SongList from 'base/song-list/song-list'
import Scroll from 'base/scroll/scroll'
import  { mapActions } from 'vuex'

const RESERVED_HEIGHT = 40

export default {
  components: {
    SongList,
    Scroll
  },
  props: {
    title: {
      type: String,
      default: ''
    },
    songs: {
      type: Array,
      default: []
    },
    bgImage: {
      type: String,
      default: ''
    }
  },
  data() {
    return {
      scrollY: 0
    }
  },
  computed: {
    bgStyle() {
      return `background-image: url(${this.bgImage})`
    }
  },
  created() {
    this.probeType = 3
    this.listenScroll = true
  },
  mounted() {
    this.imageHeight = this.$refs.bgImage.clientHeight
    this.minTransalteY = -this.imageHeight + RESERVED_HEIGHT
    console.log(this.minTransalteY, 'minTransalteY')
    this.$refs.list.$el.style.top = `${this.$refs.bgImage.clientHeight}px`
  },
  watch: {
    scrollY(newVal) {
      let translateY = Math.max(this.minTransalteY, newVal)
      this.$refs.layer.style['transform'] = `translate3d(0, ${translateY}px, 0)`
      let zIndex = 0
      let scale = 1
      let blur = 0
      let percent = Math.abs(newVal / this.imageHeight)

      if (newVal > 0) {
        //图片放大
        scale += percent
        zIndex = 10
      } else {
        //filter层模糊
        blur = Math.min(20, percent * 20)
      }

      //往上滑预留一定高度
      if (newVal < this.minTransalteY) {
        zIndex = 10
        this.$refs.bgImage.style.height = `${RESERVED_HEIGHT}px`
        this.$refs.bgImage.style.paddingTop = `0px`
        this.$refs.playBtn.style.display = 'none'
      } else {
        this.$refs.bgImage.style.height = `0px`
        this.$refs.bgImage.style.paddingTop = `70%`
        this.$refs.playBtn.style.display = ''

      }
      this.$refs.bgImage.style.zIndex = zIndex
      this.$refs.bgImage.style['transform'] = `scale(${scale})`
      this.$refs.filter.style['backdrop-filter'] = `blur(${blur}px)`
    }
  },
  methods: {
    scroll(pos) {
      this.scrollY = pos.y
      console.log(this.scrollY)
    },
    back() {
      this.$router.back()
    },
    selectItem(song, index) {
      //注意这里参数格式
      this.selectPlay({
        list: this.songs,
        index
      })
    },
    random() {
      this.randomPlay({
        list: this.songs
      })
    },
    ...mapActions([
      'selectPlay',
      'randomPlay'
    ])
  }
}
</script>

